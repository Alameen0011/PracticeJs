
//main aim is to learn how for of loop work in array and string - array case

const nums = [10,20,30]

//For Of in Array
for(let num of nums){
    console.log(num)
}

//For Of in String
const str = 'hello'

for(let char of str){
    console.log(char)
}


//For in In object
const obj = {
    a:1,
    b:2,
    c:3,
    d:4
}

for(let key in obj){
    console.log(obj[key])
}


